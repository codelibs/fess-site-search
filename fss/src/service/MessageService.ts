  /**
   * Get the language code to use
   * If fessLang is not set, use browser language.
   * If browser language is not set, use 'en'.
   * @returns Language code
   */
  getLanguage(): LanguageCode {
    // Extend Navigator interface to support legacy properties
    interface NavigatorWithLegacy extends Navigator {
      userLanguage?: string;
      browserLanguage?: string;
    }

    const nav = window.navigator as NavigatorWithLegacy;

    let lang: string =
      this.fessLang || (nav.languages && nav.languages[0]) || nav.userLanguage || nav.language || nav.browserLanguage || 'en';

    // Handle language variants (e.g., 'zh-TW' → 'tw')
    if (lang.indexOf('-') > 0) {
      if (lang === 'zh-TW') {
        lang = 'tw';
      } else if (lang === 'zh-CN' || lang === 'zh-HK') {
        lang = 'zh';
      } else {
        lang = lang.substring(0, lang.indexOf('-'));
      }
    }

    // Fallback to English if language not supported
    if (this.messages[lang as LanguageCode] === undefined) {
      lang = 'en';
    }

    return lang as LanguageCode;
  }

  /**
   * Get message from key and render with variables
   * @param key - Message key
   * @param vars - Variables to interpolate into message
   * @returns Rendered message string
   */
  get(key: MessageKey, vars?: MessageVars): string {
    const language = this.getLanguage();
    return this._getMessage(key, vars, language);
  }

  /**
   * Get message for specific language
   * @param key - Message key
   * @param vars - Variables to interpolate
   * @param language - Language code
   * @returns Rendered message string
   */
  private _getMessage(key: MessageKey, vars: MessageVars | undefined, language: LanguageCode): string {
    let message: string = this.messages[language][key];

    // Fallback to English if message not found
    if (message === undefined) {
      message = this.messages.en[key];
    }

    // Interpolate variables if provided
    if (vars) {
      for (const k in vars) {
        if (typeof vars[k] === 'string' || typeof vars[k] === 'number') {
          const reg = new RegExp(`{{${k}}}`, 'g');
          message = message.replace(reg, this._escapeHtml(vars[k]));
        }
      }
    }

    return message;
  }

  /**
   * Escape HTML special characters for security
   * @param message - Value to escape
   * @returns Escaped string
   */
  private _escapeHtml(message: string | number): string {
    if (typeof message !== 'string') {
      return String(message);
    }

    return message.replace(
      /[&'`"<>]/g,
      (match) =>
        ({
          '&': '&amp;',
          "'": '&#x27;',
          '`': '&#x60;',
          '"': '&quot;',
          '<': '&lt;',
          '>': '&gt;',
        })[match] ?? match
    );
  }
